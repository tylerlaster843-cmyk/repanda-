constexpr static uint8_t min_probes = 1;
constexpr static uint8_t max_probes = 30;
constexpr double ln2 = 0.69; // 0.69 ~ ln(2)

constexpr static size_t max_allocation_size = 128_KiB;

void create(
  uint8_t bits_per_key,
  const chunked_vector<ss::sstring>& keys,
  iobuf* filter) {
    // We intentionally round down to reduce probing cost a little bit
    auto num_probes = static_cast<uint8_t>(bits_per_key * ln2);
    num_probes = std::clamp(num_probes, min_probes, max_probes);
    // Prevent a high false positive rate by enforcing a minimum filter size.
    constexpr size_t min_bits = 64;
    size_t bits = std::max(keys.size() * bits_per_key, min_bits);
    size_t bytes = (bits + CHAR_WIDTH - 1) / CHAR_WIDTH;
    bits = bytes * CHAR_WIDTH;

    std::vector<ss::temporary_buffer<uint8_t>> chunks;
    for (size_t size = 0; size < bytes; size += max_allocation_size) {
        size_t remaining = bytes - size;
        auto& buf = chunks.emplace_back(
          std::min(remaining, max_allocation_size));
        std::fill_n(buf.get_write(), buf.size(), 0);
    }
    for (const auto& key : keys) {
        uint32_t h = XXH3_64bits(key.data(), key.size());
        // NOLINTNEXTLINE(*magic-number*)
        uint32_t delta = (h >> 17u) | (h << 15u); // Rotate right 17 bits
        for (size_t j = 0; j < num_probes; ++j) {
            uint32_t bitpos = h % bits;
            uint8_t mask = 1u << (bitpos % CHAR_WIDTH);
            size_t bytepos = bitpos / CHAR_WIDTH;
            uint8_t* chunk = chunks[bytepos / max_allocation_size].get_write();
            // NOLINTNEXTLINE(*pointer-arithmetic*)
            chunk[bytepos % max_allocation_size] |= mask;
            h += delta;
        }
    }
    for (auto& chunk : chunks) {
        filter->append(chunk.get(), chunk.size());
    }
    filter->append(std::to_array({num_probes}));
}

bool key_may_match(
  std::string_view key, block::contents& contents, size_t offset, size_t len) {
    if (len < 2) {
        return false; // Empty filter
    }
    size_t bits = (len - 1) * CHAR_WIDTH;
    // Use the encoded num_probes so that we can read filters generated by bloom
    // filters created using different parameters.
    uint8_t n_probes = contents[offset + (len - 1)];
    if (n_probes > max_probes) {
        return true;
    }
    uint32_t h = XXH3_64bits(key.data(), key.size());
    // NOLINTNEXTLINE(*magic-number*)
    uint32_t delta = (h >> 17u) | (h << 15u); // Rotate right 17 bits
    for (size_t j = 0; j < n_probes; j++) {
        uint32_t bitpos = h % bits;
        uint8_t byte = contents[offset + (bitpos / CHAR_WIDTH)];
        uint8_t mask = 1u << (bitpos % CHAR_WIDTH);
        if (!(byte & mask)) {
            return false;
        }
        h += delta;
    }
    return true;
}


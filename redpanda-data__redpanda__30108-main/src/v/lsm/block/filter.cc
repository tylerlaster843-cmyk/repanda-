filter_builder::filter_builder(options o)
  : _bits_per_key(o.bits_per_key)
  , _filter_base_lg(static_cast<uint8_t>(std::bit_width(o.filter_period) - 1))
  , _filter_base(1u << _filter_base_lg) {
    dassert(
      _filter_base == o.filter_period,
      "sst_filter_period must be a power of two, got: {}",
      o.filter_period);
}

void filter_builder::start_block(size_t block_offset) {
    uint64_t filter_index = block_offset / _filter_base;
    while (filter_index > _filter_offsets.size()) {
        generate_filter();
    }
}

void filter_builder::add_key(internal::key_view key) {
    _keys.emplace_back(key.user_key());
}

iobuf filter_builder::finish() {
    if (!_keys.empty()) {
        generate_filter();
    }
    // Append array of per-filter offsets
    uint32_t offsets_start = _filter.size_bytes();
    for (const auto& offset : std::exchange(_filter_offsets, {})) {
        _filter.append(
          std::bit_cast<std::array<uint8_t, sizeof(offset)>>(
            ss::cpu_to_le(offset)));
    }
    _filter.append(
      std::bit_cast<std::array<uint8_t, sizeof(uint32_t)>>(offsets_start));
    // Save the encoding parameter for backwards compatibility
    _filter.append(
      std::bit_cast<std::array<uint8_t, sizeof(uint8_t)>>(_filter_base_lg));
    return std::exchange(_filter, {});
}

void filter_builder::generate_filter() {
    _filter_offsets.emplace_back(_filter.size_bytes());
    if (_keys.empty()) {
        return;
    }
    bloom_filter::create(_bits_per_key, _keys, &_filter);
    _keys.clear();
}

filter_reader::filter_reader(ss::lw_shared_ptr<block::contents> c)
  : _contents(std::move(c))
  , _offset(0)
  , _num(0)
  , _base_lg(0) {
    size_t n = _contents->size();
    constexpr static size_t min_footer_size = sizeof(uint8_t)
                                              + sizeof(uint32_t);
    if (n < min_footer_size) {
        return;
    }
    _base_lg = (*_contents)[n - 1];
    auto offsets_start = _contents->read_fixed32(n - min_footer_size);
    if (offsets_start > n - min_footer_size) {
        return;
    }
    _offset = offsets_start;
    _num = (n - min_footer_size - offsets_start) / 4;
}

bool filter_reader::key_may_match(
  uint64_t block_offset, internal::key_view key) {
    uint64_t index = block_offset >> _base_lg;
    if (index < _num) {
        // TODO(perf): This is very much on the hot path, and decoding int32
        // from segmented bytes is not going to be the cheapest operation in the
        // world, maybe it makes sense to be able to decode these at
        // construction time and free the memory in _contents, with some kind of
        // `trim` operation.
        uint32_t position = _offset + (index * sizeof(uint32_t));
        auto start = _contents->read_fixed32(position);
        auto limit = _contents->read_fixed32(position + sizeof(uint32_t));
        if (start <= limit && limit <= _offset) {
            return bloom_filter::key_may_match(
              key.user_key(), *_contents, start, limit - start);
        }
    }
    return true; // Unknown/error cases we don't know
}

